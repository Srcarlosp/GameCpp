#pragma once
#include <string>
//Gestion de identificadores
class ID
{
public:
	//////////////////////////////////////////////////////////////////
	//							Atributos							//
	//////////////////////////////////////////////////////////////////
	char * ident;

	//////////////////////////////////////////////////////////////////
	//							Metodos								//
	//////////////////////////////////////////////////////////////////
	ID(char nstr[])
	{
		ident = new char[strlen(nstr) + 1];
		strcpy(ident, nstr);
	}

	~ID()
	{
		delete(ident);
	}

};

