#pragma once
#include "Vector.h"
class cVector
{
protected:
	//////////////////////////////////////////////////////////////////
	//							Atributos							//
	//////////////////////////////////////////////////////////////////
	BYTE v[3];

public:
	//////////////////////////////////////////////////////////////////
	//							Metodos								//
	//////////////////////////////////////////////////////////////////
	inline cVector(BYTE _x = 255, BYTE _y = 255, BYTE _z = 255)
	{
		v[x] = _x, v[y] = _y, v[z] = _z;
	}
	inline BYTE operator [] (const int d)  //Operador de acceso
	{
		return v[d];
	}
};

